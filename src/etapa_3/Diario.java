    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
    if (data.getText().isEmpty() || hora.getText().isEmpty() || 
        nome.getText().isEmpty()) {
           
        
        JOptionPane.showMessageDialog(this,"Por favor, preencha todos os campos.","Atenção", 
        JOptionPane.WARNING_MESSAGE);
        return;
    }
       String nomePaciente = nome.getText();
       String relatoPaciente = relato.getText();
       
       String registro = String.format("Data: %s | Hora: %s | Nome: %s | Relato: %s", 
       data.getText(), hora.getText(), nomePaciente, relatoPaciente);
        
    try (BufferedWriter writer = new BufferedWriter(new FileWriter("Relato.csv", true))) {
        writer.write(registro);
        writer.newLine();
       
        carregarHistorico();
        limparCampos();
        
        JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
      

    }catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Erro ao gravar arquivo: " + e.getMessage());
    }                
    }//GEN-LAST:event_salvarActionPerformed

