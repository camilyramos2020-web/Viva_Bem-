   private void preencherDataHoraAtual() {
        LocalDateTime agora = LocalDateTime.now();
        data.setText(agora.format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        hora.setText(agora.format(DateTimeFormatter.ofPattern("HH:mm")));
    }

 private void carregarHistorico() {
        File arquivo = new File("Relato.csv");
       
        if (!arquivo.exists()) {
            discricao.setText("Nenhum relato encontrado.");
            return;}
      
        try (BufferedReader reader = new BufferedReader(new FileReader(arquivo))) {
        StringBuilder sb = new StringBuilder();
        String linha;
        while ((linha = reader.readLine()) != null) {
        sb.append(linha).append("\n");
    }
        discricao.setText(sb.toString());
    
     } catch (IOException ex) {
        JOptionPane.showMessageDialog(this, "Erro ao ler arquivo: " + ex.getMessage());
    }
    
}
    private void limparCampos() {
       nome.setText("");
       relato.setText("");
    }

